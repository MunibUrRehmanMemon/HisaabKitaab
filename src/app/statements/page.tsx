type PeriodType = "weekly" | "monthly" | "custom";

interface StatementData {
  statement: {
    accountName: string;
    accountType: string;
    userName: string;
    period: { start: string; end: string; type: string };
    generatedAt: string;
    members: { name: string; email: string; role: string; accepted: boolean }[];
  };
  summary: {
    totalIncome: number;
    totalExpenses: number;
    netCashFlow: number;
    transactionCount: number;
    categoryBreakdown: Record<string, { income: number; expense: number }>;
  };
  transactions: {
    date: string;
    type: string;
    amount: number;
    category: string;
    categoryUr: string;
    description: string;
    source: string;
    addedBy: string;
  }[];
}

function formatPKR(amount: number): string {
  return `PKR ${amount.toLocaleString("en-PK")}`;
}

async function generatePDF(data: StatementData) {
  const { default: jsPDF } = await import("jspdf");
  const { default: autoTable } = await import("jspdf-autotable");

  const doc = new jsPDF();
  const pageWidth = doc.internal.pageSize.getWidth();

  // Header
  doc.setFillColor(15, 118, 110); // #0F766E
  doc.rect(0, 0, pageWidth, 40, "F");
  doc.setTextColor(255, 255, 255);
  doc.setFontSize(24);
  doc.text("HisaabKitaab", 14, 20);
  doc.setFontSize(10);
  doc.text(`${data.statement.accountType} Statement`, 14, 28);
  doc.text(
    `${data.statement.period.start}  to  ${data.statement.period.end}`,
    14,
    34
  );

  // Account info
  doc.setTextColor(0, 0, 0);
  doc.setFontSize(10);
  let y = 50;
  doc.text(`Account: ${data.statement.accountName}`, 14, y);
  doc.text(`Type: ${data.statement.accountType}`, 14, y + 6);
  doc.text(`Generated by: ${data.statement.userName}`, 14, y + 12);
  doc.text(
    `Generated: ${new Date(data.statement.generatedAt).toLocaleDateString("en-PK")}`,
    14,
    y + 18
  );

  // Members list
  y = 76;
  const members = data.statement.members || [];
  if (members.length > 0) {
    doc.setFontSize(13);
    doc.setTextColor(0, 0, 0);
    doc.text("Account Members", 14, y);
    y += 4;

    autoTable(doc, {
      startY: y,
      head: [["Name", "Email", "Role"]],
      body: members.map((m) => [
        m.name,
        m.email,
        m.role.charAt(0).toUpperCase() + m.role.slice(1),
      ]),
      theme: "grid",
      headStyles: { fillColor: [15, 118, 110] },
      styles: { fontSize: 9 },
      margin: { left: 14, right: 14 },
    });

    y = (doc as any).lastAutoTable.finalY + 8;
  }

  // Summary box
  doc.setDrawColor(200, 200, 200);
  doc.setFillColor(248, 250, 252); // #F8FAFC
  doc.roundedRect(14, y, pageWidth - 28, 30, 3, 3, "FD");

  doc.setFontSize(9);
  doc.setTextColor(100, 100, 100);
  const colW = (pageWidth - 28) / 4;
  const labels = ["Income", "Expenses", "Net Cash Flow", "Transactions"];
  const values = [
    formatPKR(data.summary.totalIncome),
    formatPKR(data.summary.totalExpenses),
    formatPKR(data.summary.netCashFlow),
    String(data.summary.transactionCount),
  ];
  const colors: [number, number, number][] = [
    [22, 163, 74],
    [220, 38, 38],
    data.summary.netCashFlow >= 0 ? [22, 163, 74] : [220, 38, 38],
    [15, 118, 110],
  ];

  for (let i = 0; i < 4; i++) {
    const x = 14 + colW * i + 4;
    doc.setTextColor(100, 100, 100);
    doc.text(labels[i], x, y + 10);
    doc.setFontSize(12);
    doc.setTextColor(...colors[i]);
    doc.text(values[i], x, y + 20);
    doc.setFontSize(9);
  }

  y += 38;

  // Category Breakdown
  const catEntries = Object.entries(data.summary.categoryBreakdown);
  if (catEntries.length > 0) {
    doc.setTextColor(0, 0, 0);
    doc.setFontSize(13);
    doc.text("Category Breakdown", 14, y);
    y += 4;

    autoTable(doc, {
      startY: y,
      head: [["Category", "Income (PKR)", "Expenses (PKR)"]],
      body: catEntries
        .sort(([, a], [, b]) => b.income + b.expense - (a.income + a.expense))
        .map(([cat, amounts]) => [
          cat,
          amounts.income > 0 ? amounts.income.toLocaleString("en-PK") : "-",
          amounts.expense > 0 ? amounts.expense.toLocaleString("en-PK") : "-",
        ]),
      theme: "grid",
      headStyles: { fillColor: [15, 118, 110] },
      styles: { fontSize: 9 },
      margin: { left: 14, right: 14 },
    });

    y = (doc as any).lastAutoTable.finalY + 10;
  }

  // Transaction table with "Added By" column
  doc.setTextColor(0, 0, 0);
  doc.setFontSize(13);
  doc.text(`Transactions (${data.transactions.length})`, 14, y);
  y += 4;

  if (data.transactions.length > 0) {
    autoTable(doc, {
      startY: y,
      head: [["Date", "Type", "Category", "Description", "Added By", "Amount (PKR)"]],
      body: data.transactions.map((tx) => [
        tx.date,
        tx.type.charAt(0).toUpperCase() + tx.type.slice(1),
        tx.category,
        tx.description || "-",
        tx.addedBy || "-",
        `${tx.type === "income" ? "+" : "-"}${tx.amount.toLocaleString("en-PK")}`,
      ]),
      theme: "striped",
      headStyles: { fillColor: [15, 118, 110] },
      styles: { fontSize: 8 },
      columnStyles: {
        0: { cellWidth: 22 },
        1: { cellWidth: 16 },
        2: { cellWidth: 24 },
        4: { cellWidth: 30 },
        5: { halign: "right", cellWidth: 26 },
      },
      margin: { left: 14, right: 14 },
      didParseCell: (hookData: any) => {
        if (hookData.section === "body" && hookData.column.index === 5) {
          const text = hookData.cell.raw as string;
          if (text.startsWith("+")) {
            hookData.cell.styles.textColor = [22, 163, 74];
          } else {
            hookData.cell.styles.textColor = [220, 38, 38];
          }
          hookData.cell.styles.fontStyle = "bold";
        }
      },
    });
  }

  // Footer
  const pageCount = doc.getNumberOfPages();
  for (let i = 1; i <= pageCount; i++) {
    doc.setPage(i);
    doc.setFontSize(8);
    doc.setTextColor(150, 150, 150);
    doc.text(
      `HisaabKitaab Statement - Page ${i} of ${pageCount}`,
      pageWidth / 2,
      doc.internal.pageSize.getHeight() - 8,
      { align: "center" }
    );
  }

  // Download
  const filename = `HisaabKitaab_Statement_${data.statement.period.start}_${data.statement.period.end}.pdf`;
  doc.save(filename);
}

